async def cmd_metodo(update: Update, context: ContextTypes.DEFAULT_TYPE):
    """Troca o método de retrieval para o usuário que enviou o comando."""
    chat_id = update.effective_chat.id
    opcoes_validas = {"bm25", "dense", "hibrido"}

    if context.args and context.args[0].lower() in opcoes_validas:
        metodo_usuario[chat_id] = context.args[0].lower()
        await update.message.reply_text(f"Método trocado para: {metodo_usuario[chat_id]}")
