"""---
## ETAPA 2 — Chunking: dividir o texto em trechos

### Por que fazer chunking?
LLMs e retrieval funcionam melhor com trechos **curtos e coesos** do que com documentos inteiros.  
Trechos muito curtos perdem contexto; trechos muito longos "diluem" a informação relevante.

Vamos implementar **três estratégias** diferentes:

| Estratégia | Ideia | Quando usar |
|---|---|---|
| **Por tamanho fixo** | Divide a cada N caracteres | Simples, baseline |
| **Por janela deslizante** | Cada chunk "avança" alguns caracteres, com sobreposição | Evita cortar ideias no meio |
| **Por parágrafos** | Usa as quebras de linha do Markdown como separador natural | Preserva estrutura do documento |
"""

# ── Estratégia 1: chunk de tamanho fixo ──────────────────────────────────────
def chunking_fixo(texto, tamanho=500):
    """
    Divide o texto a cada 'tamanho' caracteres.
    Simples e rápido, mas pode cortar frases no meio.
    """
    chunks = []
    inicio = 0
    while inicio < len(texto):
        fim = inicio + tamanho
        chunks.append(texto[inicio:fim])
        inicio = fim
    return chunks


# ── Estratégia 2: janela deslizante ──────────────────────────────────────────
def chunking_janela(texto, tamanho=500, sobreposicao=100):
    """
    Igual ao fixo, mas cada chunk avança (tamanho - sobreposicao) caracteres.
    A sobreposição garante que ideias no limite entre chunks não se percam.

    Atenção: sobreposicao deve ser MENOR que tamanho, senão o loop não avança.
    """
    if sobreposicao >= tamanho:
        raise ValueError(
            f"sobreposicao ({sobreposicao}) deve ser menor que tamanho ({tamanho})"
        )
    chunks = []
    passo = tamanho - sobreposicao
    inicio = 0
    while inicio < len(texto):
        fim = inicio + tamanho
        chunks.append(texto[inicio:fim])
        inicio += passo
    return chunks


# ── Estratégia 3: por parágrafos ─────────────────────────────────────────────
def chunking_paragrafo(texto, min_chars=100):
    """
    Usa as quebras duplas de linha (\n\n) como separadores naturais.
    Remove parágrafos muito curtos (menos que min_chars caracteres).
    """
    paragrafos = [p.strip() for p in texto.split("\n\n")]
    return [p for p in paragrafos if len(p) >= min_chars]


